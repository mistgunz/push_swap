#include "push_swap.h"

t_list	*ft_lstnew(int content)
{
	t_list	*rtn;

	rtn = (t_list *) malloc(sizeof(*rtn));
	if (!rtn)
		return (NULL);
	rtn->content = content;
	rtn->index = -1;
	rtn->next = NULL;
	return (rtn);
}

void	ft_lstadd_front(t_list **stack, t_list *new)
{
	if (!stack || !new)
		return ;
	new->next = *stack;
	*stack = new;
}

t_list	*ft_lstlast(t_list *head)
{
	t_list	*tmp;

	tmp = head;
	while (tmp->next)
	{
		tmp = tmp->next;
		if (tmp->next == NULL)
			return (tmp);
	}
	return (tmp);
}

void	ft_lstadd_back(t_list **stack, t_list *new)
{
	t_list	*n;

	if (*stack)
	{
		n = ft_lstlast(*stack);
		n->next = new;
		new->next = NULL;
	}
	else
	{
		*stack = new;
		(*stack)->next = NULL;
	}
}

int	ft_lstsize(t_list *head)
{
	size_t	i;
	t_list	*tmp;

	tmp = head;
	i = 0;
	while (tmp)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
